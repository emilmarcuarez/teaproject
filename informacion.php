            <li><p><strong>Síndrome de Asperger: </strong> Las personas con este síndrome generalmente tienen un buen desarrollo del lenguaje, pero pueden presentar dificultades en la comunicación social, la interacción social y pueden mostrar patrones de comportamiento repetitivos. A menudo tienen intereses específicos y pueden tener un alto funcionamiento intelectual.</p></li>

