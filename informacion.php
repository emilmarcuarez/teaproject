        <h2>Definicion</h2>
            <p>El trastorno del espectro autista (TEA) es un trastorno neurobiológico del desarrollo que afecta principalmente la comunicación social, la interacción social y el comportamiento. El TEA se caracteriza por un amplio rango de síntomas y manifestaciones, que varían en gravedad y presentación en cada individuo.</p>
            <p>El TEA es un trastorno que se manifiesta en los primeros años de vida y persiste a lo largo de toda la vida de la persona. Se considera un espectro debido a la amplia variabilidad en la gravedad y la combinación de síntomas que pueden experimentar las personas con TEA. Algunas personas pueden tener un nivel alto de funcionamiento y ser capaces de vivir de forma independiente, mientras que otras pueden tener discapacidades más significativas que requieren apoyo y cuidados especiales.</p>
       <h2>¿Que engloba el Trastorno del Espectro Autista?</h2>
        <p>El trastorno del espectro autista (TEA) engloba una serie de trastornos del neurodesarrollo que comparten características similares. Estos trastornos incluyen:</p>
